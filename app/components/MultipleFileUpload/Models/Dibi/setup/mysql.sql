CREATE TABLE `files` (
  `id` int unsigned NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `queueID` varchar(100) NOT NULL,
  `created` int NOT NULL,
  `data` text NOT NULL,
  `chunk` int NOT NULL DEFAULT 1,
  `chunks` int NOT NULL DEFAULT 1,
  `name` varchar(255) COLLATE 'utf8_general_ci' NOT NULL
) COMMENT='MultipleFileUpload files' ENGINE='InnoDB' COLLATE 'utf8_general_ci';